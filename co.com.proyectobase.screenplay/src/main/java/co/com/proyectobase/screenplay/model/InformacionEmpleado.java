	package co.com.proyectobase.screenplay.model;

public class InformacionEmpleado {

	private String firstName;
	private String middleName;
	private String lastName;
	private String location;
	private String region;
	private String fte;
	private String temporaryDepartment;
	private String bloodGroup;
	private String hobbies;
	
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getMiddleName() {
		return middleName;
	}
	public void setMiddleName(String middleName) {
		this.middleName = middleName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getRegion() {
		return region;
	}
	public void setRegion(String region) {
		this.region = region;
	}
	public String getFte() {
		return fte;
	}
	public void setFte(String fte) {
		this.fte = fte;
	}
	public String getTemporaryDepartment() {
		return temporaryDepartment;
	}
	public void setTemporaryDepartment(String temporaryDepartment) {
		this.temporaryDepartment = temporaryDepartment;
	}
	public String getBloodGroup() {
		return bloodGroup;
	}
	public void setBloodGroup(String bloodGroup) {
		this.bloodGroup = bloodGroup;
	}
	public String getHobbies() {
		return hobbies;
	}
	public void setHobbies(String hobbies) {
		this.hobbies = hobbies;
	}
	

}
